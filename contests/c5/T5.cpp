#include <iostream>
#include <string>
#include <map>
#include <set>
#include <deque>
using namespace std;

struct FSM{
    const int numStates;
    const int numAlphabet;

    deque<int> states;
    deque<int> freeStates;

    map<int,map<char,set<int>>>transitions;
    set<int>visited;

    FSM(const int numAlphabet, const int numStates) : numStates(numStates), numAlphabet(numAlphabet) {
    }

    void addEPS(const int from){
        if (visited.find(from)!=visited.end())return;
        visited.insert(from);
        for(auto to : transitions[from]['E']) addEPS(to);
        visited.erase(from);
        freeStates.push_front(from);
    }

    void addEPS(){
        freeStates.clear();
        visited.clear();

        for (auto from:states) {
            visited.insert(from);
            for(auto to:transitions[from]['E'])addEPS(to);
            visited.erase(from);
        }
        for(auto state: freeStates)states.push_front(state);

    }

    void reload(){
        states.clear();
        freeStates.clear();
        states.push_back(0);
        visited.clear();
    }

    void addTransition(int from,int to,char t){
        if (from>=numStates || from < 0 || to <0 || to >=numStates)return;
        transitions[from][t].insert(to);
    }

    bool step(char& t){
        if (int(t)-97>=numAlphabet || int (t)-97<0)return false;

        addEPS();

        const int size = states.size();

        for (int i = 0; i <size; ++i) {
            if (states.empty())return false;
            int from = states.back();
            states.pop_back();
            for(auto to: transitions[from][t])states.push_front(to);
        }
        return !states.empty();
    }
};

bool canRead(string& s,FSM& fsm,set<int>& states){
    fsm.reload();
    for (auto letter:s)if (!fsm.step(letter))return false;
    fsm.addEPS();
    for(auto j:fsm.states)
        if (states.find(j)!=states.end())return true;
    return false;
}

int main(){
    int alphabet,state,transition,terminate,request;

    cin >> alphabet >> state >> transition;

    FSM fsm(alphabet,state);

    for (int i = 0; i < transition; ++i) {
        int q,w;
        char c;
        cin>>q>>w>>c;
        fsm.addTransition(q,w,c);
    }

    cin >> terminate;
    set<int> states;
    for (int i = 0; i < terminate ; ++i) {
        int q;
        cin>>q;
        states.insert(q);
    }

    cin>>request;
    string s;
    for (int i = 0; i <request ; ++i) {
        cin>>s;
        cout<<canRead(s,fsm,states);
        if (i!=request-1)cout<<' ';
    }

    return 0;
}
